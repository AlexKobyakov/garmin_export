# Извлечение стилей из QGIS (импорт qgis только внутри функций)
# ----------------------------------------------------------------------

def _mm_to_pixels(width_mm):
    """Пересчёт толщины линии из мм (QGIS) в пиксели устройства Garmin.

    Экраны навигаторов имеют плотность ~90-130 dpi; используем
    практичное соотношение ~4 px на мм с ограничением 1..6.
    """
    try:
        width = float(width_mm)
    except (TypeError, ValueError):
        return 2
    return max(1, min(6, int(round(width * 4)) or 1))


def _qcolor_to_hex(qcolor):
    """QColor -> '#rrggbb' (альфа игнорируется для линий/полигонов)"""
    try:
        return '#{0:02x}{1:02x}{2:02x}'.format(
            qcolor.red(), qcolor.green(), qcolor.blue())
    except Exception:
        return '#808080'


def _first_symbol(layer):
    """Первый символ рендерера слоя (для категорий - символ первой категории)"""
    renderer = layer.renderer()
    if renderer is None:
        return None
    try:
        symbols = renderer.symbols(None)
    except TypeError:
        try:
            from qgis.core import QgsRenderContext
            symbols = renderer.symbols(QgsRenderContext())
        except Exception:
            symbols = []
    except Exception:
        symbols = []
    if symbols:
        return symbols[0]
    try:
        return renderer.symbol()
    except AttributeError:
        return None


def _marker_symbol_pixels(symbol, size=16):
    """Рендер маркерного символа в матрицу пикселей для XPM"""
    try:
        from qgis.PyQt.QtCore import QSize
        image = symbol.asImage(QSize(size, size))
    except Exception:
        return None

    if image is None or image.isNull():
        return None

    pixels = []
    for y in range(image.height()):
        row = []
        for x in range(image.width()):
            rgba = image.pixelColor(x, y)
            if rgba.alpha() < 128:
                row.append(None)
            else:
                row.append('#{0:02x}{1:02x}{2:02x}'.format(
                    rgba.red(), rgba.green(), rgba.blue()))
        pixels.append(row)

    # Полностью прозрачная иконка бесполезна
    if all(pixel is None for row in pixels for pixel in row):
        return None

    return pixels


def extract_layer_style(layer):
    """Извлечение стиля слоя QGIS.

    Returns:
        словарь: {'kind': 'polygon'|'line'|'point', 'color': '#rrggbb',
                  'width_px': int, 'border_color': str|None,
                  'pixels': матрица|None}
        или None, если стиль извлечь не удалось.
    """
    from qgis.core import QgsWkbTypes

    symbol = _first_symbol(layer)
    if symbol is None:
        return None

    geom_type = layer.geometryType()

    if geom_type == QgsWkbTypes.PolygonGeometry:
        color = _qcolor_to_hex(symbol.color())
        return {'kind': 'polygon', 'color': color,
                'width_px': 0, 'border_color': None, 'pixels': None}

    if geom_type == QgsWkbTypes.LineGeometry:
        color = _qcolor_to_hex(symbol.color())
        width_px = 2
        try:
            width_px = _mm_to_pixels(symbol.width())
        except AttributeError:
            pass
        return {'kind': 'line', 'color': color,
                'width_px': width_px, 'border_color': None, 'pixels': None}

    if geom_type == QgsWkbTypes.PointGeometry:
        color = _qcolor_to_hex(symbol.color())
        pixels = _marker_symbol_pixels(symbol)
        return {'kind': 'point', 'color': color,
                'width_px': 0, 'border_color': None, 'pixels': pixels}

    return None


def build_typ_from_layers(layer_entries, family_id, product_id, code_page,
                          log_callback=None):
    """Построение typ.txt из списка слоёв.

    Args:
        layer_entries: список словарей
            {'layer': QgsVectorLayer, 'garmin_type': '0x16',
             'geometry_type': 'Polygon'}
            в порядке отрисовки сверху вниз (как в дереве слоёв)
        family_id, product_id, code_page: параметры карты
        log_callback: функция для сообщений
    Returns:
        содержимое typ.txt (str)
    """
    builder = TypFileBuilder(family_id, product_id, code_page)

    seen_types = {'polygon': set(), 'line': set(), 'point': set()}
    total = len(layer_entries)

    for index, entry in enumerate(layer_entries):
        layer = entry.get('layer')
        garmin_type = entry.get('garmin_type', '0x01')
        try:
            type_code = int(str(garmin_type), 16)
        except ValueError:
            if log_callback:
                log_callback('TYP: некорректный тип Garmin "{0}" для слоя "{1}"'.format(
                    garmin_type, entry.get('name', '?')))
            continue

        style = None
        if layer is not None:
            try:
                style = extract_layer_style(layer)
            except Exception as e:
                if log_callback:
                    log_callback('TYP: не удалось прочитать стиль слоя "{0}": {1}'.format(
                        entry.get('name', '?'), str(e)))

        if style is None:
            continue

        kind = style['kind']
        if type_code in seen_types[kind]:
            continue
        seen_types[kind].add(type_code)

        label = entry.get('name') or None

        if kind == 'polygon':
            # Слои выше в дереве рисуются поверх: больший draw_order
            draw_order = max(1, min(MAX_DRAW_ORDER, total - index))
            builder.add_polygon(type_code, style['color'], draw_order, label)
        elif kind == 'line':
            builder.add_line(type_code, style['color'],
                             style['width_px'], label=label)
        elif kind == 'point':
            if style['pixels']:
                builder.add_point(type_code, style['pixels'], label)
            # POI без иконки оставляем стандартному стилю Garmin

    return builder.build()
