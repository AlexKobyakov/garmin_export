"""
TYP Generator for Garmin Export Plugin
Генератор TYP-файлов (typ.txt) из символики слоёв QGIS

TYP-файл определяет внешний вид объектов на устройстве Garmin (цвета
полигонов, толщину и цвет линий, иконки POI). Текстовый typ.txt
компилируется самим mkgmap: достаточно передать его дополнительным
входным файлом. Формат описан в "TYP Compiler Manual" mkgmap.

Модуль разделён на две части:
  - TypFileBuilder: чистый построитель текста typ.txt (без QGIS),
    полностью тестируемый;
  - extract_layer_style / build_typ_from_layers: чтение символики QGIS.

Author: Кобяков Александр Викторович (Alex Kobyakov)
Email: kobyakov@lesburo.ru
Year: 2025-2026
"""


# Символы для палитры XPM (без кавычек и обратного слэша)
_XPM_CHARS = (
    'abcdefghijklmnopqrstuvwxyz'
    'ABCDEFGHIJKLMNOPQRSTUVWXYZ'
    '0123456789'
    '.,:;!?#$%&*+-/<=>@[]^_`{|}~ '
)

MAX_DRAW_ORDER = 8


def _normalize_hex_color(color, default='#808080'):
    """Приведение цвета к виду #rrggbb"""
    if not color:
        return default
    text = str(color).strip()
    if not text.startswith('#'):
        text = '#' + text
    if len(text) == 4:  # #rgb -> #rrggbb
        text = '#' + ''.join(ch * 2 for ch in text[1:])
    if len(text) >= 7:
        return text[:7].lower()
    return default


class TypFileBuilder:
    """Построитель текстового TYP-файла (typ.txt) для компиляции mkgmap"""

    def __init__(self, family_id, product_id=1, code_page='1252'):
        self.family_id = int(family_id)
        self.product_id = int(product_id)
        self.code_page = str(code_page)
        self.polygons = []   # (type_code, color, draw_order, label)
        self.lines = []      # (type_code, color, width, border_color, border_width, label)
        self.points = []     # (type_code, pixels, label)

    # ------------------------------------------------------------------
    # Добавление элементов
    # ------------------------------------------------------------------

    def add_polygon(self, type_code, color, draw_order=3, label=None):
        """Полигон со сплошной заливкой.

        Args:
            type_code: числовой тип Garmin (например 0x16)
            color: '#rrggbb'
            draw_order: порядок отрисовки 1..8 (выше = поверх)
            label: подпись типа (необязательно)
        """
        order = max(1, min(MAX_DRAW_ORDER, int(draw_order)))
        self.polygons.append(
            (int(type_code), _normalize_hex_color(color), order, label))

    def add_line(self, type_code, color, width=2, border_color=None,
                 border_width=0, label=None):
        """Линия заданной толщины (в пикселях устройства).

        Args:
            type_code: числовой тип Garmin (например 0x06)
            color: '#rrggbb' основной цвет
            width: толщина линии в пикселях (1..15)
            border_color: цвет каймы или None
            border_width: толщина каймы в пикселях
        """
        width = max(1, min(15, int(round(width))))
        border_width = max(0, min(10, int(round(border_width))))
        self.lines.append((
            int(type_code), _normalize_hex_color(color), width,
            _normalize_hex_color(border_color) if border_color else None,
            border_width, label))

    def add_point(self, type_code, pixels, label=None):
        """POI с иконкой.

        Args:
            type_code: числовой тип Garmin (например 0x2f00)
            pixels: матрица (список строк) значений '#rrggbb', '#rrggbbaa'
                или None (прозрачный пиксель); все строки одной длины
            label: подпись типа (необязательно)
        """
        if not pixels or not pixels[0]:
            return
        self.points.append((int(type_code), pixels, label))

    # ------------------------------------------------------------------
    # Генерация текста
    # ------------------------------------------------------------------

    def build(self):
        """Возвращает содержимое typ.txt"""
        blocks = [
            '; Generated by Garmin Export Plugin for QGIS',
            '; TYP source file for mkgmap TYP compiler',
            '',
            '[_id]',
            'FID={0}'.format(self.family_id),
            'ProductCode={0}'.format(self.product_id),
            'CodePage={0}'.format(self.code_page),
            '[end]',
            '',
        ]

        # [_drawOrder]: полигон не из списка не отображается вовсе,
        # поэтому секция обязательна при наличии полигонов
        if self.polygons:
            blocks.append('[_drawOrder]')
            for type_code, _color, order, _label in self.polygons:
                blocks.append('Type={0},{1}'.format(
                    self._format_type(type_code), order))
            blocks.append('[end]')
            blocks.append('')

        for type_code, color, _order, label in self.polygons:
            blocks.extend(self._polygon_block(type_code, color, label))
            blocks.append('')

        for entry in self.lines:
            blocks.extend(self._line_block(*entry))
            blocks.append('')

        for type_code, pixels, label in self.points:
            blocks.extend(self._point_block(type_code, pixels, label))
            blocks.append('')

        return '\n'.join(blocks)

    @staticmethod
    def _format_type(type_code):
        """Форматирование кода типа: 0xNN или расширенный 0xNNNNN"""
        if type_code > 0xFFFF:
            return '0x{0:05x}'.format(type_code)
        if type_code > 0xFF:
            return '0x{0:04x}'.format(type_code)
        return '0x{0:02x}'.format(type_code)

    def _polygon_block(self, type_code, color, label):
        lines = [
            '[_polygon]',
            'Type={0}'.format(self._format_type(type_code)),
        ]
        if label:
            lines.append('String={0}'.format(label))
        lines += [
            'Xpm="0 0 1 0"',
            '"a c {0}"'.format(color),
            '[end]',
        ]
        return lines

    def _line_block(self, type_code, color, width, border_color,
                    border_width, label):
        lines = [
            '[_line]',
            'Type={0}'.format(self._format_type(type_code)),
        ]
        if label:
            lines.append('String={0}'.format(label))
        lines.append('LineWidth={0}'.format(width))
        if border_color and border_width > 0:
            lines.append('BorderWidth={0}'.format(border_width))
            lines.append('Xpm="0 0 2 0"')
            lines.append('"a c {0}"'.format(color))
            lines.append('"b c {0}"'.format(border_color))
        else:
            lines.append('Xpm="0 0 1 0"')
            lines.append('"a c {0}"'.format(color))
        lines.append('[end]')
        return lines

    def _point_block(self, type_code, pixels, label):
        height = len(pixels)
        width = len(pixels[0])

        palette, rows = self._build_xpm_palette(pixels)

        lines = [
            '[_point]',
            'Type={0}'.format(self._format_type(type_code)),
        ]
        if label:
            lines.append('String={0}'.format(label))
        lines.append('DayXpm="{0} {1} {2} 1"'.format(
            width, height, len(palette)))
        for char, color in palette:
            lines.append('"{0} c {1}"'.format(char, color))
        for row in rows:
            lines.append('"{0}"'.format(row))
        lines.append('[end]')
        return lines

    @staticmethod
    def _build_xpm_palette(pixels):
        """Построение палитры XPM из матрицы пикселей.

        Возвращает (palette, rows):
            palette: список (символ, цвет) где цвет '#rrggbb',
                '#rrggbbaa' или 'none'
            rows: строки пиксельной карты
        """
        color_to_char = {}
        palette = []
        rows = []

        def register(color_value):
            if color_value in color_to_char:
                return color_to_char[color_value]
            index = len(palette)
            if index >= len(_XPM_CHARS):
                # Палитра переполнена - огрубляем цвет (4 бита на канал)
                if color_value not in (None, 'none') and len(color_value) >= 7:
                    coarse = '#' + ''.join(
                        color_value[i] * 2 for i in (1, 3, 5))
                    if coarse in color_to_char:
                        color_to_char[color_value] = color_to_char[coarse]
                        return color_to_char[coarse]
                    color_value = coarse
                    index = len(palette)
                    if index >= len(_XPM_CHARS):
                        return palette[0][0]
                else:
                    return palette[0][0]
            char = _XPM_CHARS[index]
            palette.append((char, color_value))
            color_to_char[color_value] = char
            return char

        for row in pixels:
            row_chars = []
            for pixel in row:
                if pixel is None:
                    row_chars.append(register('none'))
                else:
                    row_chars.append(register(pixel.lower()))
            rows.append(''.join(row_chars))

        return palette, rows


